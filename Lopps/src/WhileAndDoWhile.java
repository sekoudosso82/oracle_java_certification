public class WhileAndDoWhile {

    public static void main(String[] args) {
        int count = 0 ;
        while (count < 5 ){
            System.out.println("hello word");
            count++;
        }

        int count2 = 0 ;
        do {
            System.out.println("Do while Hello World");
            count2++;
        } while ( count2 < 5 );
    }
}
